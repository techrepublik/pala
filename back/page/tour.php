							at high tide the island disappears and at low tide, it reappears.
						</p>
						<p class="p-format"><strong>Cowrie Island</strong> is named after the small-to-large sea snail, marine gastropod mollusc called "cowrie."  
							The island has a number of cabanas, a pavillion and thatched-roof spa huts, all over looking the sea and 
							creating a beautiful, picturesque scene. Cowrie Island has recently opened to the public as an alternative to 
							the crowded Pandan Island, situated nearby.
						</p>
